# Conversion in different numeric types:
a0 = -72
a1 = 2.25
print( "%d(an int), in float & complex is" % (a0) , "\n" , float(a0) , "&"  , "\n" , complex(a0) )
print( "a0 still equals" , a0 )
print( "%.2f(a float), in int & complex is" % (a1) , "\n" , int(a1) , "&"  , "\n" , complex(a1) )
print( "a1 still equals" , a1 )
#Complex numbers can neither be represented in form of integers, nor float
